void AES_Encrypt(unsigned char* message, unsigned char* key){
    unsigned char state[16];

    //copy first 16 blocks to state
    for(int i = 0; i < 16; i++){
        state[i] = message[i];
    }


    int numberOfRounds = 9;
    //this doesn't, but should expand the keys only once and use them outside of AES_encrypt
    unsigned char expandedKey[176];
    KeyExpansion(key, expandedKey);
    AddRoundKey(state, key); // AddRoundKey


    for(int i = 0; i < numberOfRounds; i++){
        SubBytes(state);
        ShiftRows(state);
        MixColumns(state);
        AddRoundKey(state, expandedKey + (16 * (i + 1)));
    }

    // This is the Final Round
    SubBytes(state);
    ShiftRows(state);
    AddRoundKey(state, expandedKey+160);

    //copy over message with encrypted message
    //should copy over into new variable to as not to lose the original message.
    for(int i = 0; i < 16; i++){
        message[i] = state[i];
    }
}


