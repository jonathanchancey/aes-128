void KeyExpansionCore(unsigned char* in, unsigned char i){
    //rotate left smart way
    unsigned int* q = (unsigned int*)in;
    *q = (*q >> 8) | ((*q & 0xff) << 24);
    //rotate left
//    unsigned char t = in[0];
//    in[0] = in[1];
//    in[1] = in[2];
//    in[2] = in[3];
//    in[3] = t;

    //s-box four bytes
    in[0] = s_box[in[0]];
    in[1] = s_box[in[1]];
    in[2] = s_box[in[2]];
    in[3] = s_box[in[3]];

    //rcon
    //the i value will never be more than like the 11th value in the rcon table
    in[0] ^= rcon[i];

}


void KeyExpansion(unsigned char* inputKey, unsigned char* expandedKeys){
    //first 16 bytes originate from the original key
    for(int i = 0; i < 16; i++){
        expandedKeys[i] = inputKey[i];
    }

    //vars
    int bytesGenerated = 16; // 16 bytes already generated by copying the orignal key
    int rconIteration = 1; // rcon iteration starts at 1
    unsigned char temp[4]; // temp for storage

    while (bytesGenerated < 176){
        //reads 4 bytes into tmp array
        for(int i = 0; i < 4; i++){
            temp[i] = expandedKeys[i + bytesGenerated - 4];
        }
        //once every 16 bytes/every new key
        if(bytesGenerated % 16 == 0){
            KeyExpansionCore(temp, rconIteration);
            rconIteration++;
        }

        //XOR temp with [bytesGenerates-16] and stores in new expandedKeys
        for(unsigned char a = 0; a < 4; a++){
            expandedKeys[bytesGenerated] = expandedKeys[bytesGenerated-16] ^ temp[a];
            bytesGenerated++;
        }
    }


}

